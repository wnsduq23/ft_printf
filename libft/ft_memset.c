#include <stddef.h>

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*pb;

	i = 0;
	pb = (unsigned char *)b;
	while (i < len)
		pb[i++] = c;
	return ((void *)pb);
}
